def pertambahan(nilai1, nilai2):
    hasil = nilai1 + nilai2
    print(f"hasil dari {nilai1} + {nilai2} = {hasil}")

def pengurangan(nilai1, nilai2):
    hasil = nilai1 - nilai2
    print(f"hasil dari {nilai1} - {nilai2} = {hasil}")

def perkalian(nilai1, nilai2):
    hasil = nilai1 * nilai2
    print(f"hasil dari {nilai1} * {nilai2} = {hasil}")

def pembagian(nilai1, nilai2):
    hasil = nilai1 / nilai2
    print(f"hasil dari {nilai1} / {nilai2} = {hasil}")
